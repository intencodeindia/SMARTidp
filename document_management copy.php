                            <h2 class="headline -large ">FAQs on document management systems (DMS)</h2>
                        </div>
                        <section class="ce_ ce_accordion ce_text block">
                            <div class="toggler">
                                What is document management? </div>
                            <div class="accordion">
                                <div>
                                    Document management refers to the software-assisted administration of electronic documents over their entire life cycle. A document management system is part of a broader ECM platform and serves as the single source of truth for all information.
                                </div>
                            </div>
                        </section>
                        <section class="ce_ ce_accordion ce_text block">
                            <div class="toggler">
                                What does DMS stand for? </div>
                            <div class="accordion">
                                <div>
                                    DMS stands for document management system. It is software that is designed specifically for capturing, managing, processing and storing documents.
                                </div>
                            </div>
                        </section>
                        <section class="ce_ ce_accordion ce_text block">
                            <div class="toggler">
                                What does document management cover? </div>
                            <div class="accordion">
                                <div>
                                    A DMS looks after a document over its entire life cycle, from creation, processing and editing, to audit-proof archiving and deletion at the end of its retention period. Modern systems also support users with additional automation capabilities or other
                                    features such as task assignment, authorization concepts and machine learning.
                                </div>
                            </div>
                        </section>
                        <section class="ce_ ce_accordion ce_text block">
                            <div class="toggler">
                                Why is a DMS important? </div>
                            <div class="accordion">
                                <div>
                                    Since many business processes depend on documents, these must meet legal as well as internal requirements — including speedy retrieval, audit-proof archiving and simple sharing. A conventional file storage system simply isn’t enough. You need a customizable
                                    system with best practice workflows.
                                </div>
                            </div>
                        </section>
                        <section class="ce_ ce_accordion ce_text block">
                            <div class="toggler">
                                What are the advantages of a DMS? </div>
                            <div class="accordion">
                                <div>
                                    A DMS is the connecting link between departments and helps to improve digital collaboration. All document-driven processes benefit from greater transparency while becoming faster and fully compliant. Defined workflows and the automation of individual
                                    tasks unlock the greatest potential for reducing costs and enhancing productivity.
                                </div>
                            </div>
                        </section>
